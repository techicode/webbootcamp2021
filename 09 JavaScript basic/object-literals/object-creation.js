// create an object
const person = {
    firstName: 'Mike',
    lastName: 'Peperson',
    alive: true,
    age: 25
}

// how to access the data of an object
console.log(person['firstName']); // Mike
console.log(person.firstName); // Mike

console.log(person['age']); // 25
console.log(person.age); // 25

